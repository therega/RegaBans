package fun.rega.RegaBans.utils;


public class MRep {
  private String key;
  
  private String value;
  
  public MRep(String key, String value) {
    this.key = key;
    this.value = value;
  }
  
  public String getKey() {
    return this.key;
  }
  
  public String getValue() {
    return this.value;
  }
}
